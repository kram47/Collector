    private void refreshVocabulary() throws SQLException
    {
        LinkedList<String> words = Indexador.getVocabularyList();
            
        DefaultListModel listModel = new DefaultListModel();
        for (String word : words)
        {
            listModel.addElement(word);
        }
        tab2VocabularyList.setModel(listModel);
    }
    
