    private void addUrl()
    {
        String new_url = tab3SitesAddText.getText();
        
        if (!new_url.isEmpty() && !urls.contains(new_url))
        {
            Pattern p=Pattern.compile("((http:\\/\\/|https:\\/\\/)?(www.)?(([a-zA-Z0-9-]){2,}\\.){1,4}([a-zA-Z]){2,6}(\\/([a-zA-Z-_\\/\\.0-9#:?=&;,]*)?)?)");
            Matcher m=p.matcher(new_url);
            if(m.find())
            {
                urls.add(new_url);
                DefaultListModel listModel = new DefaultListModel();
                for (String url : urls)
                    listModel.addElement(url);
                tab3SitesList.setModel(listModel);
                tab3SitesAddText.setText("");
            }
            else
            {
                JOptionPane.showMessageDialog(null, "O site deve ser de uma forma correta !\n"
                        + "Exemplos : http://www.meusite.com, www.meusite.com, http://www.meusite.com/pagina.php, ... \n"
                        + "Regex : ((http:\\/\\/|https:\\/\\/)?(www.)?(([a-zA-Z0-9-]){2,}\\.){1,4}([a-zA-Z]){2,6}(\\/([a-zA-Z-_\\/\\.0-9#:?=&;,]*)?)?)");
            }
        }
    }
    
